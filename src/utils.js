export const SendToS3 = async (fl, loc) => {
  const file = fl;
  const { name, type } = fl;
  const folderName = loc + `/` + name;
  try {
    await Storage.put(folderName, file, {
      level: `public`,
      contentType: type
    });
  } catch (error) {
    console.log('Upload to S3 ERROR: ', error);
    return false;
  }
};
